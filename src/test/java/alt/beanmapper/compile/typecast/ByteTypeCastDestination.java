package alt.beanmapper.compile.typecast;

/**
 * 
 * @author Albert Shift
 *
 */

public class ByteTypeCastDestination {

	private byte booleanValue;
	private byte byteValue;
	private byte charValue;
	private byte shortValue;
	private byte intValue;
	private byte longValue;
	private byte floatValue;
	private byte doubleValue;

	public byte getBooleanValue() {
		return booleanValue;
	}

	public void setBooleanValue(byte booleanValue) {
		this.booleanValue = booleanValue;
	}

	public byte getByteValue() {
		return byteValue;
	}

	public void setByteValue(byte byteValue) {
		this.byteValue = byteValue;
	}

	public byte getCharValue() {
		return charValue;
	}

	public void setCharValue(byte charValue) {
		this.charValue = charValue;
	}

	public byte getShortValue() {
		return shortValue;
	}

	public void setShortValue(byte shortValue) {
		this.shortValue = shortValue;
	}

	public byte getIntValue() {
		return intValue;
	}

	public void setIntValue(byte intValue) {
		this.intValue = intValue;
	}

	public byte getLongValue() {
		return longValue;
	}

	public void setLongValue(byte longValue) {
		this.longValue = longValue;
	}

	public byte getFloatValue() {
		return floatValue;
	}

	public void setFloatValue(byte floatValue) {
		this.floatValue = floatValue;
	}

	public byte getDoubleValue() {
		return doubleValue;
	}

	public void setDoubleValue(byte doubleValue) {
		this.doubleValue = doubleValue;
	}

}
