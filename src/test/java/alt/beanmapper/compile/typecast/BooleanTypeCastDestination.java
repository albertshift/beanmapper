package alt.beanmapper.compile.typecast;

/**
 * 
 * @author Albert Shift
 *
 */

public class BooleanTypeCastDestination {

	private boolean booleanValue;
	private boolean byteValue;
	private boolean charValue;
	private boolean shortValue;
	private boolean intValue;
	private boolean longValue;
	private boolean floatValue;
	private boolean doubleValue;

	public boolean isBooleanValue() {
		return booleanValue;
	}

	public void setBooleanValue(boolean booleanValue) {
		this.booleanValue = booleanValue;
	}

	public boolean isByteValue() {
		return byteValue;
	}

	public void setByteValue(boolean byteValue) {
		this.byteValue = byteValue;
	}

	public boolean isCharValue() {
		return charValue;
	}

	public void setCharValue(boolean charValue) {
		this.charValue = charValue;
	}

	public boolean isShortValue() {
		return shortValue;
	}

	public void setShortValue(boolean shortValue) {
		this.shortValue = shortValue;
	}

	public boolean isIntValue() {
		return intValue;
	}

	public void setIntValue(boolean intValue) {
		this.intValue = intValue;
	}

	public boolean isLongValue() {
		return longValue;
	}

	public void setLongValue(boolean longValue) {
		this.longValue = longValue;
	}

	public boolean isFloatValue() {
		return floatValue;
	}

	public void setFloatValue(boolean floatValue) {
		this.floatValue = floatValue;
	}

	public boolean isDoubleValue() {
		return doubleValue;
	}

	public void setDoubleValue(boolean doubleValue) {
		this.doubleValue = doubleValue;
	}

}
