public class FloatTypeCastDestination {

	private float booleanValue;
	private float byteValue;
	private float charValue;
	private float shortValue;
	private float intValue;
	private float longValue;
	private float floatValue;
	private float doubleValue;

	public float getBooleanValue() {
		return booleanValue;
	}

	public void setBooleanValue(float booleanValue) {
		this.booleanValue = booleanValue;
	}

	public float getByteValue() {
		return byteValue;
	}

	public void setByteValue(float byteValue) {
		this.byteValue = byteValue;
	}

	public float getCharValue() {
		return charValue;
	}

	public void setCharValue(float charValue) {
		this.charValue = charValue;
	}

	public float getShortValue() {
		return shortValue;
	}

	public void setShortValue(float shortValue) {
		this.shortValue = shortValue;
	}

	public float getIntValue() {
		return intValue;
	}

	public void setIntValue(float intValue) {
		this.intValue = intValue;
	}

	public float getLongValue() {
		return longValue;
	}

	public void setLongValue(float longValue) {
		this.longValue = longValue;
	}

	public float getFloatValue() {
		return floatValue;
	}

	public void setFloatValue(float floatValue) {
		this.floatValue = floatValue;
	}

	public float getDoubleValue() {
		return doubleValue;
	}

	public void setDoubleValue(float doubleValue) {
		this.doubleValue = doubleValue;
	}

}
