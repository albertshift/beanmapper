public class CharTypeCastDestination {

	private char booleanValue;
	private char byteValue;
	private char charValue;
	private char shortValue;
	private char intValue;
	private char longValue;
	private char floatValue;
	private char doubleValue;

	public char getBooleanValue() {
		return booleanValue;
	}

	public void setBooleanValue(char booleanValue) {
		this.booleanValue = booleanValue;
	}

	public char getByteValue() {
		return byteValue;
	}

	public void setByteValue(char byteValue) {
		this.byteValue = byteValue;
	}

	public char getCharValue() {
		return charValue;
	}

	public void setCharValue(char charValue) {
		this.charValue = charValue;
	}

	public char getShortValue() {
		return shortValue;
	}

	public void setShortValue(char shortValue) {
		this.shortValue = shortValue;
	}

	public char getIntValue() {
		return intValue;
	}

	public void setIntValue(char intValue) {
		this.intValue = intValue;
	}

	public char getLongValue() {
		return longValue;
	}

	public void setLongValue(char longValue) {
		this.longValue = longValue;
	}

	public char getFloatValue() {
		return floatValue;
	}

	public void setFloatValue(char floatValue) {
		this.floatValue = floatValue;
	}

	public char getDoubleValue() {
		return doubleValue;
	}

	public void setDoubleValue(char doubleValue) {
		this.doubleValue = doubleValue;
	}

}
