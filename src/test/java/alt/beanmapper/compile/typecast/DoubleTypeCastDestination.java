public class DoubleTypeCastDestination {

	private double booleanValue;
	private double byteValue;
	private double charValue;
	private double shortValue;
	private double intValue;
	private double longValue;
	private double floatValue;
	private double doubleValue;

	public double getBooleanValue() {
		return booleanValue;
	}

	public void setBooleanValue(double booleanValue) {
		this.booleanValue = booleanValue;
	}

	public double getByteValue() {
		return byteValue;
	}

	public void setByteValue(double byteValue) {
		this.byteValue = byteValue;
	}

	public double getCharValue() {
		return charValue;
	}

	public void setCharValue(double charValue) {
		this.charValue = charValue;
	}

	public double getShortValue() {
		return shortValue;
	}

	public void setShortValue(double shortValue) {
		this.shortValue = shortValue;
	}

	public double getIntValue() {
		return intValue;
	}

	public void setIntValue(double intValue) {
		this.intValue = intValue;
	}

	public double getLongValue() {
		return longValue;
	}

	public void setLongValue(double longValue) {
		this.longValue = longValue;
	}

	public double getFloatValue() {
		return floatValue;
	}

	public void setFloatValue(double floatValue) {
		this.floatValue = floatValue;
	}

	public double getDoubleValue() {
		return doubleValue;
	}

	public void setDoubleValue(double doubleValue) {
		this.doubleValue = doubleValue;
	}

}
